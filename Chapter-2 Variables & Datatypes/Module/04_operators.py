# Arithmetic operators : +, -, *, / etc.
a = 10
b = 20
c = a+b
print(c)

# Assignment operators : +=, -+, *=, /= etc.
a = 4-2 # Assign 4-2 in a
print(a)

b = 6
b += 3 # Increment the value of b by 3 and then assign it to b
print(b)

# Comparison operators : ==, >, =>, <, <=, != etc.
a = 5<4
print(a)

# Logical operatros
e = True or False
print(e)

# Truth table of 'or'
print("Ture or False is: ", True or False)
print("Ture or False is: ", True or True)
print("Ture or False is: ", False or True)
print("Ture or False is: ", False or False)

# Truth table of 'and'
print("Ture or False is: ", True and False)
print("Ture or False is: ", True and True)
print("Ture or False is: ", False and True)
print("Ture or False is: ", False and False)

# 'and' operatros
print(not(False))
