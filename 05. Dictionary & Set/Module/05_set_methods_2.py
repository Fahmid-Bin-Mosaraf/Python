set_a = {1, 2, 3}
set_b = {3, 4, 5}
print(set_a.union(set_b)) # Return a set containing the union of sets

print(set_a.intersection(set_b)) # Returns a set, that is the intersection of two other sets

print(set_a.difference(set_b)) # Returns a set containing the difference between two or more sets

print(set_a.symmetric_difference(set_b)) # Returns a set with the symmetric differences of two sets

